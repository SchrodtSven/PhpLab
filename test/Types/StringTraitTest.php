    public function testIfBeginsEndsContainsWorxProperly(string $origin, string $bg, string $md, string $en): void
    {
        $s = new StringClass($origin);
        $this->assertTrue($s->begins($bg));
        $this->assertTrue($s->ends($en));
        $this->assertTrue($s->contains($md));
    }

    // MultibyteStringTrait


    #[DataProvider('legacyIpNumSubstringProvider')]
    public function testIfLegacyIpNumSubstrWorxProperly(int $start, int $length, string $address, string $substr): void
    {
        $this->assertSame(
            (string) (new StringClass($address))->substr($start, $length),
            $substr
        );
    }

    #[DataProvider('splitCountProvider')]
    public function testSubstrCountPlusSplit(string $str, int $count): void
    {
        $needle = '|';
        $strC = new StringClass($str);
        $this->assertSame($strC->substrCount($needle), $count);
        $tmp = $strC->splitBy($needle);
        $this->assertSame(count($tmp), $count + 1);
    }


    // StringTransformationTrait

    
    #[DataProvider('stringCaseProvider')]
    public function testIfCaseTransformationWorxPorperly(
        string $original,
        string $camel,
        string $snaky,
        string $lc,
        string $uc,
        string $ucf,

    ) {
        $strC = new StringClass($original);
        $this->assertSame((string) $strC->camelize(' '), $camel);
        $this->assertSame((string) $strC->snakify(), $snaky);
        $this->assertSame((string) $strC->lower(), $lc);
        $this->assertSame((string) $strC->upper(), $uc);
        $this->assertSame((string) $strC->lower()->upperFirst(), $ucf);
    }


